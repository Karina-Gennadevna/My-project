import Anthropic from '@anthropic-ai/sdk'
import { NextRequest, NextResponse } from 'next/server'

const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY })

export async function POST(req: NextRequest) {
  try {
    const data = await req.json()
    const { name, index, scores, answers } = data

    if (!answers || !Array.isArray(answers) || answers.length === 0) {
      return NextResponse.json({ error: 'No quiz data' }, { status: 400 })
    }

    const answersText = ['K', 'A', 'Q', 'S'].map(axis => {
      const axisAnswers = answers.filter((a: { axis: string }) => a.axis === axis)
      const axisName = axisAnswers[0]?.axisName || axis
      const lines = axisAnswers.map((a: { question: string; chosen: string; score: number; maxScore: number }) =>
        `  — ${a.question}\n    Ответ: «${a.chosen}» (${a.score} из ${a.maxScore})`
      ).join('\n')
      return `ОСЬ ${axis} — ${axisName} (итог: ${scores[axis]}%):\n${lines}`
    }).join('\n\n')

    const prompt = `Ты — продуктовый архитектор и эксперт по систематизации бизнеса.

Тебе переданы РЕАЛЬНЫЕ ответы владельца бизнеса на диагностику K-AQS™.
Составь Roadmap уровня дорогого консалтинга (50k+) — конкретный, жёсткий, с привязкой к деньгам.

СТИЛЬ — СТРОГО:
— короткие фразы, прямые формулировки
— без "рекомендуется", "возможно", "стоит", "улучшить", "оптимизировать"
— без воды и общих мест
— каждая проблема = деньги + последствие
— писать как для владельца, не как консультант

ДАННЫЕ ДИАГНОСТИКИ:
Владелец: ${name || 'не указано'}
Индекс управляемости: ${index}/100
Оси: K=${scores.K}% | A=${scores.A}% | Q=${scores.Q}% | S=${scores.S}%

${answersText}

---

ТРЕБОВАНИЯ К КАЖДОМУ БЛОКУ:

БЛОК status:
— label: бьющий статус, не нейтральный
  НЕ "бизнес работает, но теряет деньги"
  А "система не управляется — деньги утекают" или "вы теряете деньги из-за отсутствия контроля"
— summary: 3 вывода — строго в формате "проблема → к чему это приводит"
  каждый вывод должен ощущаться как причинно-следственная связь, не как описание
  пример: "нет регламентов → каждый сотрудник принимает решения по-своему → качество непредсказуемо"
  кратко, жёстко, без воды

БЛОК losses:
Рассчитай потери строго по 4 категориям. Для каждой — разложи логику, покажи формулу, посчитай сумму.
Цифры не из воздуха — они должны вытекать из реальных ответов диагностики.
Никаких диапазонов. Одна конкретная цифра.

КАТЕГОРИЯ 1 — Время сотрудников:
— найди в ответах: нет регламентов, ручные операции, дублирование, уточнения
— формула: (часы потерь в день) × (кол-во сотрудников) × (рабочих дней) × (средняя стоимость часа)
— средняя стоимость часа: малый бизнес 300–500 ₽, средний 500–800 ₽
— поле formula: покажи расчёт словами, например "2 ч × 3 чел × 22 дня × 400 ₽ = 52 800 ₽"

КАТЕГОРИЯ 2 — Потери лидов:
— найди в ответах: нет CRM, ручная обработка, нет скриптов, слабые продажи
— формула: (кол-во лидов в месяц) × (% потерь) × (средний чек)
— кол-во лидов: оценить по типу бизнеса (малый: 20–50, средний: 50–200)
— % потерь при хаосе: 20–35%
— поле formula: "40 лидов × 25% потерь × 15 000 ₽ чек = 150 000 ₽"

КАТЕГОРИЯ 3 — Ошибки и потери клиентов:
— найди в ответах: нет контроля качества, жалобы, AI без проверки, слабый сервис
— формула: (кол-во клиентов) × (% уходящих из-за ошибок) × (средний LTV)
— LTV: средний чек × среднее кол-во покупок в год
— поле formula: "100 клиентов × 5% уходят × 30 000 ₽ LTV = 15 000 ₽"

КАТЕГОРИЯ 4 — Управленческие потери:
— найди в ответах: нет метрик, нет стратегии, хаотичные решения, нет делегирования
— формула: (часы руководителя на операционку) × (стоимость часа руководителя) × (рабочих дней)
— стоимость часа руководителя: 1 000–3 000 ₽
— поле formula: "3 ч × 22 дня × 1 500 ₽ = 99 000 ₽"
— поле cause должно раскрывать 3 уровня:
  1. что происходит (руководитель в операционке)
  2. как это влияет на решения (решения без данных, наугад)
  3. к чему это приводит в бизнесе (бизнес не растёт, топчется на месте)
  формат cause: "X → Y → Z" одной строкой

ИТОГ:
— monthly = сумма всех 4 категорий
— yearly = monthly × 12
— для малого бизнеса итог: 80 000–300 000 ₽/мес, среднего: 200 000–700 000 ₽/мес

БЛОК problems:
— 3-5 проблем максимум
— только из реальных ответов
— каждая: что конкретно не работает → к чему это приводит (деньги / хаос / потери)
— формулировки жёсткие: "ломает систему", "гробит конверсию", а не "не идеально"

БЛОК root_cause:
— illusion: "Вы думаете, что проблема в X"
— reality: "На самом деле Y — и пока это не исправить, остальные действия не дадут результата"
— сделать сдвиг мышления, убрать мягкость, добавить конфликт

БЛОК priorities:
— critical: 2-3 действия — конкретные, не абстрактные
— important: 2-3 действия
— later: 2-3 доработки
— каждое действие — глагол + объект (не "улучшить", а "зафиксировать / закрыть / убрать")

БЛОК roadmap:
— 3 этапа по 3-4 действия
— в начале каждого этапа: что изменится после его выполнения (поле "result")
— в конце каждого этапа: поле "dependency" — одна жёсткая фраза о том, почему без этого этапа следующий не сработает
  пример: "без базы процессов автоматизация только закрепит хаос"
  коротко, конкретно, без мягкости
— каждое действие содержит:
  action: конкретное действие (не абстрактное)
  if_not: если не сделать → конкретное последствие (деньги / хаос / срыв), без мягкости

БЛОК first_step:
— один конкретный шаг, который можно сделать сегодня
— action: что именно сделать (не "проанализируйте", а конкретное действие)
— time: сколько займёт (конкретно: "20 минут", "1 час", "2 часа")
— effect: что изменится сразу — формат "это займёт X, но даст Y результат"
  Y должен быть конкретным и ощутимым, не абстрактным

---

Верни JSON строго в этом формате (без markdown, только JSON):
{
  "status": {
    "index": ${index},
    "label": "бьющий статус",
    "summary": ["вывод с деньгами/хаосом 1", "вывод 2", "вывод 3"]
  },
  "losses": {
    "monthly": "итоговая сумма ₽ (сумма всех 4 категорий)",
    "yearly": "итоговая сумма × 12 ₽",
    "breakdown": [
      {
        "category": "Время сотрудников",
        "cause": "что конкретно происходит по ответам",
        "formula": "X ч × Y чел × Z дней × R ₽ = сумма",
        "amount": "X ₽"
      },
      {
        "category": "Потери лидов",
        "cause": "причина из ответов",
        "formula": "X лидов × Y% × Z ₽ чек = сумма",
        "amount": "X ₽"
      },
      {
        "category": "Ошибки и потери клиентов",
        "cause": "причина из ответов",
        "formula": "X клиентов × Y% × Z ₽ LTV = сумма",
        "amount": "X ₽"
      },
      {
        "category": "Управленческие потери",
        "cause": "причина из ответов",
        "formula": "X ч × Y дней × Z ₽/ч = сумма",
        "amount": "X ₽"
      }
    ]
  },
  "problems": [
    {"problem": "конкретная проблема", "consequence": "жёсткое последствие"}
  ],
  "root_cause": {
    "illusion": "Вы думаете, что проблема в X",
    "reality": "На самом деле Y — и пока это не исправить, остальные действия не дадут результата"
  },
  "priorities": {
    "critical": ["глагол + объект 1", "глагол + объект 2"],
    "important": ["действие 1", "действие 2"],
    "later": ["доработка 1", "доработка 2"]
  },
  "roadmap": [
    {
      "phase": "Этап 1 · 0–30 дней",
      "goal": "цель одной строкой",
      "result": "что изменится после выполнения этапа",
      "dependency": "без этого этапа следующий не даст результата — жёсткая конкретная причина",
      "actions": [
        {"action": "конкретное действие", "if_not": "конкретное последствие с деньгами или хаосом"}
      ]
    },
    {
      "phase": "Этап 2 · 30–60 дней",
      "goal": "цель",
      "result": "что изменится",
      "dependency": "без этого следующий этап не сработает — причина",
      "actions": [
        {"action": "конкретное действие", "if_not": "последствие"}
      ]
    },
    {
      "phase": "Этап 3 · 60–90 дней",
      "goal": "цель",
      "result": "что изменится",
      "dependency": "без этого рост остановится — причина",
      "actions": [
        {"action": "конкретное действие", "if_not": "последствие"}
      ]
    }
  ],
  "first_step": {
    "action": "конкретный шаг сегодня",
    "time": "сколько займёт (например: 30 минут)",
    "effect": "это займёт X, но даст Y конкретный результат"
  }
}`

    const message = await client.messages.create({
      model: 'claude-opus-4-6',
      max_tokens: 2500,
      messages: [{ role: 'user', content: prompt }]
    })

    const text = message.content[0].type === 'text' ? message.content[0].text : ''

    const jsonMatch = text.match(/\{[\s\S]*\}/)
    if (!jsonMatch) {
      return NextResponse.json({ error: 'Invalid AI response' }, { status: 500 })
    }

    const roadmap = JSON.parse(jsonMatch[0])
    return NextResponse.json({ roadmap })

  } catch (err) {
    console.error('Roadmap API error:', err)
    return NextResponse.json({ error: 'Internal error' }, { status: 500 })
  }
}
