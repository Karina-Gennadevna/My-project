"""
11 агентов AI-копирайтера.
Каждый агент — async функция с системным промптом, моделью и температурой.

Агенты:
  Диспетчер       — маршрутизирует запрос к нужному пайплайну
  Оркестратор     — координирует агентов внутри пайплайна
  Архитектор      — планирует структуру поста
  Декомпозитор    — разбивает сложные задачи на шаги
  Аналитик стиля  — извлекает Tone of Voice из примеров
  Ресёрчер        — ищет факты и углы для темы
  Копирайтер      — пишет черновик поста
  Критик          — оценивает пост по шкале
  Редактор        — правит: короче, живее, без хэштегов
  Тестировщик     — финальная QA-проверка
  Контент-планер  — выдаёт темы из плана
"""

import json
from openai import AsyncOpenAI
from config import (
    OPENROUTER_API_KEY, OPENROUTER_BASE_URL,
    MODEL_SONNET, MODEL_OPUS, MODEL_HAIKU,
    DATA_DIR, EXAMPLES_DIR,
)

client = AsyncOpenAI(
    api_key=OPENROUTER_API_KEY,
    base_url=OPENROUTER_BASE_URL,
)

HEADERS = {
    "HTTP-Referer": "https://kontent-marketing-bot",
    "X-Title": "Kontent Marketing Bot",
}


# ─── Утилиты ──────────────────────────────────────────────────────────────────

def load_file(filename: str) -> str:
    path = DATA_DIR / filename
    return path.read_text(encoding="utf-8") if path.exists() else ""


def load_examples() -> str:
    texts = []
    for file in sorted(EXAMPLES_DIR.iterdir()):
        if file.suffix == ".md":
            texts.append(f"=== {file.name} ===\n{file.read_text(encoding='utf-8')[:3000]}")
        elif file.suffix == ".json":
            try:
                data = json.loads(file.read_text(encoding="utf-8"))
                if isinstance(data, dict) and "messages" in data:
                    posts = [
                        m["text"] for m in data["messages"]
                        if m.get("type") == "message" and isinstance(m.get("text"), str) and m["text"].strip()
                    ]
                    sample = "\n\n---\n\n".join(posts[:40])
                    texts.append(f"=== {file.name} (Telegram export, {len(posts)} постов) ===\n{sample[:4000]}")
            except Exception:
                pass
    return "\n\n".join(texts) if texts else "Примеры постов не загружены."


async def _call(system: str, user: str, model: str, temperature: float) -> str:
    resp = await client.chat.completions.create(
        model=model,
        temperature=temperature,
        messages=[
            {"role": "system", "content": system},
            {"role": "user",   "content": user},
        ],
        extra_headers=HEADERS,
    )
    return resp.choices[0].message.content.strip()


# ══════════════════════════════════════════════════════════════════════════════
# АГЕНТ 1 — ДИСПЕТЧЕР
# Модель: Haiku | Темп: 0.2
# Принимает запрос, маршрутизирует к нужному пайплайну
# ══════════════════════════════════════════════════════════════════════════════

async def dispatcher(message: str) -> str:
    """
    Возвращает одно из: write_post | edit_post | analyze_style |
                         content_plan | decompose | unknown
    """
    system = """Ты диспетчер. Определи тип запроса и верни СТРОГО одно слово:

write_post     — написать новый пост (тема, идея, запрос на создание)
edit_post      — пользователь прислал готовый текст и хочет его улучшить
analyze_style  — проанализировать стиль, обновить Tone of Voice
content_plan   — показать темы из контент-плана
decompose      — задача сложная, нужно разбить на шаги
unknown        — всё остальное

Только одно слово. Без объяснений."""

    result = await _call(system, message, MODEL_HAIKU, 0.2)
    valid = {"write_post", "edit_post", "analyze_style", "content_plan", "decompose", "unknown"}
    return result.strip().lower() if result.strip().lower() in valid else "write_post"


# ══════════════════════════════════════════════════════════════════════════════
# АГЕНТ 2 — ОРКЕСТРАТОР
# Модель: Sonnet | Темп: 0.3
# Координирует агентов, принимает решения внутри пайплайна
# ══════════════════════════════════════════════════════════════════════════════

async def orchestrator_decide(context: str, question: str) -> str:
    """Принимает решение на развилке — какой следующий шаг нужен."""
    system = """Ты оркестратор команды агентов. Принимаешь решения на развилках пайплайна.
Отвечай кратко и конкретно — только решение, без воды."""
    return await _call(system, f"Контекст:\n{context}\n\nВопрос: {question}", MODEL_SONNET, 0.3)


# ══════════════════════════════════════════════════════════════════════════════
# АГЕНТ 3 — АРХИТЕКТОР
# Модель: Opus | Темп: 0.7
# Планирует структуру поста, ключевые решения
# ══════════════════════════════════════════════════════════════════════════════

async def architect(task: str) -> str:
    tov      = load_file("tone-of-voice.md")
    audience = load_file("audience.md")
    plan     = load_file("контент-план.md")

    system = f"""Ты архитектор контента. Планируешь структуру поста — текст НЕ пишешь.

АУДИТОРИЯ И БОЛИ:
{audience[:1500]}

КОНТЕНТ-ПЛАН (ветки и пропорции):
{plan[:1000]}

TONE OF VOICE (правила стиля):
{tov[:800]}

Выдай структуру:
- Ветка: (🧠 Экспертная / 🛠 Продуктовая / 👤 Личная / 💰 Продающая)
- Сегмент аудитории: (кому конкретно)
- Боль/мечта/страх, которую закрывает:
- Формат: (история / кейс / чеклист / до-после / мнение / инструкция)
- Крючок-идея: (первое предложение, останавливающее скроллинг)
- Тезисы: (3–4 ключевые мысли)
- CTA: (конкретный призыв к действию)"""

    return await _call(system, f"Задача: {task}", MODEL_OPUS, 0.7)


# ══════════════════════════════════════════════════════════════════════════════
# АГЕНТ 4 — ДЕКОМПОЗИТОР
# Модель: Sonnet | Темп: 0.3
# Разбивает сложные задачи на мелкие шаги
# ══════════════════════════════════════════════════════════════════════════════

async def decomposer(task: str) -> str:
    system = """Ты декомпозитор. Разбивай любую задачу на атомарные шаги.

Формат:
1. [Агент] — [конкретное действие]
2. [Агент] — [конкретное действие]
...

Агенты: Архитектор, Ресёрчер, Копирайтер, Критик, Редактор, Тестировщик.
Тестировщик — всегда последний. Ресёрчер — если нужны факты/примеры."""

    return await _call(system, f"Задача: {task}", MODEL_SONNET, 0.3)


# ══════════════════════════════════════════════════════════════════════════════
# АГЕНТ 5 — АНАЛИТИК СТИЛЯ
# Модель: Sonnet | Темп: 0.5
# Анализирует примеры постов, извлекает Tone of Voice
# ══════════════════════════════════════════════════════════════════════════════

async def style_analyst() -> str:
    examples = load_examples()

    system = """Ты аналитик стиля. Изучаешь примеры постов автора и извлекаешь его Tone of Voice.

Проанализируй:
1. ЛЕКСИКА — слова-маркеры, обороты, паразиты, которые делают автора узнаваемым
2. РИТМ — длина предложений, как строится мысль, паузы и связки
3. ОБЪЯСНЕНИЕ — через что объясняет: аналогии, цифры, истории, сценарии
4. ТОНУС — насколько эмоционален, уверен, мягок
5. ОТНОШЕНИЕ К ЧИТАТЕЛЮ — учит, делится, рассуждает вместе?

Выдай:
- Описание стиля (5–7 предложений)
- 5 правил письма в этом стиле
- 3 слова/фразы, которые НЕЛЬЗЯ использовать
- 5 фраз-маркеров, которые делают автора узнаваемым"""

    if examples == "Примеры постов не загружены.":
        return "⚠️ Примеры постов не загружены. Загрузи .md или .json с постами канала."

    return await _call(system, f"Примеры постов автора:\n\n{examples}", MODEL_SONNET, 0.5)


# ══════════════════════════════════════════════════════════════════════════════
# АГЕНТ 6 — РЕСЁРЧЕР
# Модель: Sonnet | Темп: 0.4
# Собирает факты, углы и примеры для темы поста
# ══════════════════════════════════════════════════════════════════════════════

async def researcher(topic: str) -> str:
    system = """Ты ресёрчер. Готовишь фактуру для поста — факты, цифры, примеры, углы подачи.

Выдай:
- 3–5 конкретных факта или статистики по теме (с источником если знаешь)
- 2–3 реальных сценария/примера из практики бизнеса
- 3 угла подачи темы (как можно зайти в пост по-разному)
- Контринтуитивный факт или неожиданный поворот по теме

Контекст: ниша — систематизация малого бизнеса и внедрение AI.
Будь конкретным. Никаких обобщений вроде "многие компании"."""

    return await _call(system, f"Тема поста: {topic}", MODEL_SONNET, 0.4)


# ══════════════════════════════════════════════════════════════════════════════
# АГЕНТ 7 — КОПИРАЙТЕР
# Модель: Sonnet | Темп: 0.8
# Пишет текст поста в стиле автора
# ══════════════════════════════════════════════════════════════════════════════

async def copywriter(structure: str, research: str = "") -> str:
    tov      = load_file("tone-of-voice.md")
    examples = load_examples()

    research_block = f"\nФАКТУРА ОТ РЕСЁРЧЕРА:\n{research}\n" if research else ""

    system = f"""Ты копирайтер Telegram-канала. Пишешь посты голосом автора.

ПРИМЕРЫ ПОСТОВ (учись стилю — это эталон):
{examples[:3000]}

ПРАВИЛА СТИЛЯ:
{tov[:1500]}
{research_block}
СТРОГИЕ ПРАВИЛА:
- Крючок в первых 1–2 предложениях — должен остановить скроллинг
- Структура: крючок → тело → вывод → CTA
- 150–300 слов
- Разговорный язык, как будто думаешь вслух рядом с читателем
- Конкретные цифры и примеры — не теория
- 3–5 эмодзи там, где усиливают смысл (не для украшения)
- Фразы-маркеры: "то есть", "по факту", "получается", "смотрите"
- ЗАПРЕЩЕНО: "осуществляем", "в рамках", "реализуем", хэштеги (#)"""

    return await _call(system, f"Напиши пост по этой структуре:\n\n{structure}", MODEL_SONNET, 0.8)


# ══════════════════════════════════════════════════════════════════════════════
# АГЕНТ 8 — КРИТИК
# Модель: Sonnet | Темп: 0.4
# Оценивает пост по шкале, даёт конкретную ревизию
# ══════════════════════════════════════════════════════════════════════════════

async def critic(post: str) -> str:
    tov = load_file("tone-of-voice.md")

    system = f"""Ты критик контента. Оцениваешь пост по шкале и даёшь конкретную ревизию.

ЭТАЛОН СТИЛЯ:
{tov[:800]}

Оцени по каждому критерию от 1 до 10:
- Крючок (останавливает скроллинг?)
- Стиль автора (соответствует ToV?)
- Конкретность (цифры, примеры, детали?)
- Структура (крючок → тело → вывод → CTA?)
- Живость (звучит как человек, а не ChatGPT?)

Формат ответа:
ОЦЕНКИ:
Крючок: X/10
Стиль: X/10
Конкретность: X/10
Структура: X/10
Живость: X/10
ИТОГО: X/10

РЕВИЗИЯ:
[Конкретные замечания — что именно слабо и как улучшить]

ВЕРДИКТ: ПУБЛИКОВАТЬ / ДОРАБОТАТЬ"""

    return await _call(system, f"Оцени пост:\n\n{post}", MODEL_SONNET, 0.4)


# ══════════════════════════════════════════════════════════════════════════════
# АГЕНТ 9 — РЕДАКТОР
# Модель: Sonnet | Темп: 0.6
# Правит по запросу: короче, живее, без хэштегов
# ══════════════════════════════════════════════════════════════════════════════

async def editor(text: str, instruction: str = "") -> str:
    tov = load_file("tone-of-voice.md")

    default_instruction = (
        "Сделай текст короче и живее. "
        "Убери хэштеги. Убери канцелярит. "
        "Сохрани смысл и стиль автора."
    )
    directive = instruction if instruction.strip() else default_instruction

    system = f"""Ты редактор. Правишь текст по конкретной инструкции — не переписываешь с нуля.

СТИЛЬ АВТОРА:
{tov[:800]}

Правило: отдавай только финальный текст — без комментариев, объяснений и пометок."""

    return await _call(
        system,
        f"Инструкция: {directive}\n\nТекст:\n{text}",
        MODEL_SONNET, 0.6,
    )


# ══════════════════════════════════════════════════════════════════════════════
# АГЕНТ 10 — ТЕСТИРОВЩИК
# Модель: Haiku | Темп: 0.1
# Финальная QA-проверка перед публикацией
# ══════════════════════════════════════════════════════════════════════════════

async def tester(post: str) -> str:
    system = """Ты тестировщик. Проверяешь пост по чеклисту перед публикацией.

ЧЕКЛИСТ:
□ Крючок в первых 1–2 предложениях
□ Структура: крючок → тело → вывод → CTA
□ 150–300 слов
□ Нет канцелярита и корпоратива
□ Есть конкретная цифра или пример
□ Эмодзи: 3–5, не больше
□ Нет хэштегов
□ CTA конкретный (не "подписывайтесь")

Ответ СТРОГО в одном из двух форматов:
ПРИНЯТО
или
ДОРАБОТАТЬ: [список замечаний по пунктам]"""

    return await _call(system, f"Проверь пост:\n\n{post}", MODEL_HAIKU, 0.1)


# ══════════════════════════════════════════════════════════════════════════════
